	public static void waitingTo(String msg, double time) {
		System.out.println();
		System.out.print(msg + " ");
		for (int i = 1; i < 100; i++) {
			if ((i - 1) % 20 == 0) {
				System.out.println();
			}
			String s = i + "%; ";
			System.out.print(s);
			if (i == 100) {
				break;
			}
			int totalSleep = 1 * 100;
			// int totalSleep = 100 * 101 / 2; // = time
			double f = time / totalSleep;
			// System.out.println(f); // debug
			try {
				// Thread.sleep((long) ((100 - i) * f)); // Bar: 100, 99, 98... 3, 2, 1
				Thread.sleep((long) ((1) * f));
			} catch (InterruptedException e) {
				// System.err.println(e.getMessage());
			}
			// String j = String.valueOf(i);
			for (int k = 0; k < s.length(); k++) {
				System.out.print("\b");
			}
		}
		System.out.print("100%...   ");
		System.out.println();
		System.out.println();
		System.out.println();
	}

